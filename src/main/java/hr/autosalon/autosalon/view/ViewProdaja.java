    private void ucitaj(){
        DefaultListModel<Prodaja> m = new DefaultListModel<>();
        obrada.getPodaci().forEach(s -> m.addElement(s));
        lstPodaci.setModel(m);
    }
    
    private void ucitajVrijednosti(){
        
        obrada.getEntitet().setProdavac(cmbProdavac.getModel()
        .getElementAt(cmbProdavac.getSelectedIndex()));
        obrada.getEntitet().setVozilo(cmbVozilo.getModel()
        .getElementAt(cmbVozilo.getSelectedIndex()));
        obrada.getEntitet().setKupac(cmbKupac.getModel()
        .getElementAt(cmbKupac.getSelectedIndex()));
        if(dpDatumProdaje.getDate()!=null){
            Date d = Pomocno.convertToDateViaInstant(dpDatumProdaje.getDate());
            obrada.getEntitet().setDatumProdaje(d);
    }
    }

    private void postaviVrijednosti(){
        
        cmbProdavac.setSelectedItem(obrada.getEntitet().getProdavac());
        cmbVozilo.setSelectedItem(obrada.getEntitet().getVozilo());
        cmbKupac.setSelectedItem(obrada.getEntitet().getKupac());
        if(obrada.getEntitet().getDatumProdaje()==null){
            dpDatumProdaje.setDate(null);
        }else{
            dpDatumProdaje.setDate(Pomocno.convertToLocalDateViaInstant(obrada.getEntitet().getDatumProdaje()));
        }
        
    }
