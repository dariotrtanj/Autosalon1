    private void ucitajProdavace() {
        DefaultComboBoxModel<Prodavac> m = new DefaultComboBoxModel<>();
        new ObradaProdavac().getPodaci().forEach(s -> m.addElement(s));
        cmbProdavac.setModel(m);
    }
    
